public class Calcul {
 
/** additionne a avec b 
 * 
 * @param a
 * @param b
 * @return
 */
	
	public int additionner(int a, int b){
		return a+b;
	}
/** Soustraire b de a */	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
